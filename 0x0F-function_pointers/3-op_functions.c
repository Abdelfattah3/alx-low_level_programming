#include "3-calc.h"
#include <stdio.h>
/**
* op_add - addition operation
* @a : int
* @b : int
* Return: an int
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - substraction operation
* @a : int
* @b : int
* Return: an int
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - multiplication operation
* @a : int
* @b : int
* Return: an int
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - division operation
* @a : int
* @b : int
* Return: an int
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - modulation operation
* @a : int
* @b : int
* Return: an int
*/
int op_mod(int a, int b)
{
	return (a % b);
}
